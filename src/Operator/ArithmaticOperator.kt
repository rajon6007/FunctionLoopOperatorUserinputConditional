package Operator

fun main()

{
    var a = 100
    var b = 50
    //Add
    var sum = a + b
    println("sum: $sum")

    //sub
    var sub = a - b
    println("sub: $sub")

    //Multiplication
    var multi = a * b
    println("multi: $multi")

    //division
    var div = a / b
    println("div: $div")

    //modulas
    var mod = a % b
    println("mod: $mod")



}